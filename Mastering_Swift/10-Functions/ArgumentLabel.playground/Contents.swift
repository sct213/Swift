import UIKit

/*:
 # Argument Label
 */

func sayHello(name: String) {
   print("Hello, \(name)")
}

sayHello(name: "Swift")

func sayHello(to name: String) {
    print("Hello, \(name)")
}
sayHello(to: "Swift")

func sayHello(_ name: String) {
    print("Hello, \(name)")
}

sayHello("Swift")



